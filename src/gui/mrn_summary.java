    private void jTextField7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField7ActionPerformed
        String txt = jTextField7.getText();
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        ResultSet rs = data.search("SELECT * FROM `min` WHERE `oredr_no` = '" + txt + "'");
        try {
            while(rs.next()){
                Vector<String> vr  = new Vector<>();
                vr.add(rs.getString("minNo"));
                vr.add(rs.getString("mrnNo"));
                vr.add(rs.getString("description"));
                vr.add(rs.getString("qty"));
                vr.add(rs.getString("Total"));
                vr.add(rs.getString("date"));
                dtm.addRow(vr);
            
                ResultSet rs1 = data.search("SELECT SUM(Total) From `min` WHERE `oredr_no` = '"+txt+"'");
                if(rs1.next()){
                jTextField8.setText(rs1.getString(1));
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Enter The Data", "Inform", JOptionPane.INFORMATION_MESSAGE);
            Log.warning(e);
        }
    }//GEN-LAST:event_jTextField7ActionPerformed

