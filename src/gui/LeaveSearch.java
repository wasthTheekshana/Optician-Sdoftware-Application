    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        String nic = jTextField1.getText();
        ResultSet rs = data.search("SELECT * FROM `leaving` WHERE `employee_nic` = '" + nic + "'");
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            while (rs.next()) {

                ResultSet rs1 = data.search("SELECT COUNT(employee_nic) From `leaving` WHERE `employee_nic` = '" + nic + "'");
                if (rs1.next()) {
//                    jTextField3.setText(rs1.getString(1).trim());

                    Vector<String> vr = new Vector<>();
                    vr.add(rs.getString("employee_nic"));
                    vr.add(rs.getString("month"));
                    vr.add(rs.getString("Reason"));
                    vr.add(rs.getString("date"));
                    dtm.addRow(vr);
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Enter The Date", "Inform", JOptionPane.INFORMATION_MESSAGE);
            Log.warning(e);
            System.out.println(e);
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

