    public void loadTable() {

        ResultSet rs = data.search("SELECT * FROM `invoice`");
        TableModel tm = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) tm;
        dtm.setRowCount(0);
        try {
            while (rs.next()) {
                Vector<String> vr = new Vector<>();

                ResultSet rs1 = data.search("SELECT * FROM `customer` WHERE `id` = '" + rs.getString("customer_id") + "'");
                if (rs1.next()) {
                    vr.add(rs1.getString("personal_Id"));
                    vr.add(rs1.getString("name"));
                }

                vr.add(rs.getString("invoice_number"));
                vr.add(rs.getString("payment"));
                vr.add(rs.getString("advance_pay"));
                vr.add(rs.getString("date"));
                vr.add(rs.getString("discount"));
                dtm.addRow(vr);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }

