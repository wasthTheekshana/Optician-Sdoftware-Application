   public void loadcustomer() {
        ResultSet rs = data.search("SELECT * FROM `customer`");
        TableModel tm = customer_reg.jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) tm;
        dtm.setRowCount(0);

        try {
            while (rs.next()) {
                Vector<String> vr = new Vector<>();
                vr.add(rs.getString("mobile"));
                vr.add(rs.getString("name"));
                vr.add(rs.getString("email"));
                vr.add(rs.getString("address"));

                dtm.addRow(vr);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }
    
    
    public void report(){
    try {
             String path = "src//reports//salary.jasper";
    
                     String id1 = viewSalary.no6.getText();
        String name = viewSalary.no7.getText();
        String basic = viewSalary.no8.getText();
        String etf = viewSalary.no9.getText();
        String epf = viewSalary.no11.getText();
        String net = viewSalary.no13.getText();
             
        HashMap<String, Object> map = new HashMap<>();
            map.put("Parameter1", id1);
            map.put("Parameter2", name);
            map.put("Parameter3", basic);
            map.put("Parameter4", etf);
            map.put("Parameter5", epf);
            map.put("Parameter6", net);


            JREmptyDataSource dataSource = new JREmptyDataSource();

            JasperPrint print = JasperFillManager.fillReport(path, map, dataSource);
            JRViewer viewer = new JRViewer(print);
            jPanel1.add(viewer);
            jPanel1.revalidate();
        } catch (Exception e) {
            System.out.println(e);
        }
        
    
    
    }
    
    
